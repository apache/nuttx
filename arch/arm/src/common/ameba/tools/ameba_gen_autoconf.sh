#!/usr/bin/env bash
############################################################################
# arch/arm/src/common/ameba/tools/ameba_gen_autoconf.sh
#
# Regenerate the AP-core platform_autoconf.h from the SDK menuconfig and stage
# it (with an include guard) at the path NuttX consumes.
#
# Why: the SDK flash layout / feature switches (CONFIG_FLASH_VFS1_OFFSET,
# CONFIG_WHC_*, ...) live in the SDK Kconfig.  Rather than commit a hand-made
# platform_autoconf.h snapshot (which silently drifts when the SDK menuconfig
# changes, and is absent on a clean clone), we regenerate it from the SDK on
# every build.  The SDK is then the single source of truth: change the layout
# in `ameba.py menuconfig <SOC>` and NuttX (ld script, WiFi force-include, VFS1
# partition geometry) follows automatically.
#
# `ameba.py menuconfig <SOC> -r` writes the default .config and regenerates the
# per-core headers at build_<SOC>/menuconfig/project_<core>/platform_autoconf.h.
# The SDK header ships WITHOUT an include guard, but ameba_lwip_off.h's
# `#undef CONFIG_LWIP_LAYER` relies on the autoconf being processed exactly once
# (many SDK headers re-#include it).  So we wrap the generated header in a guard
# while staging it.
#
# usage: ameba_gen_autoconf.sh <sdk_dir> <soc> <ap_project> <out_header>
#   sdk_dir     - ameba-rtos checkout
#   soc         - ameba.py SoC id (RTL8721Dx / RTL8720F / ...)
#   ap_project  - AP-core project subdir name (km4 on amebadplus, km4tz on 8720F)
#   out_header  - destination path for the guarded platform_autoconf.h
############################################################################

set -e

SDK="$1"
SOC="$2"
AP_PROJECT="$3"
OUT="$4"

if [ -z "$SDK" ] || [ -z "$SOC" ] || [ -z "$AP_PROJECT" ] || [ -z "$OUT" ]; then
  echo "usage: ameba_gen_autoconf.sh <sdk_dir> <soc> <ap_project> <out_header>" >&2
  exit 1
fi

# Use the python dir ameba_setup_env.sh resolved (a real .venv when one
# exists, otherwise a system-python3 shim); fall back to system python3.
PYDIR=$(cat "$SDK/.amebapy/bindir" 2>/dev/null)
VENV_PY="$PYDIR/python"
[ -x "$VENV_PY" ] || VENV_PY=python3

# Put that bin dir FIRST on PATH so the SDK tooling's bare `python` calls
# (setconfig.py / kconfiglib) resolve to the same interpreter.
PATH="${PYDIR:+$PYDIR:}$PATH"
export PATH

# Isolate from NuttX's Kconfig environment (BINDIR/srctree/KCONFIG_CONFIG ...),
# which otherwise leaks into the SDK's own kconfiglib and breaks its Kconfig
# path resolution.  Also drop the make jobserver fds.
unset srctree BINDIR APPSDIR APPSBINDIR EXTERNALDIR EXTERNDIR KCONFIG_CONFIG \
      ARCHDIR DRIVERDIR BOARDDIR 2>/dev/null || true
unset MAKEFLAGS MAKELEVEL MFLAGS MAKEOVERRIDES 2>/dev/null || true

# ameba.py resolves build_<SOC>/ relative to the CWD, so run from the SDK root.
cd "$SDK" || { echo "ameba_gen_autoconf.sh: cannot cd to $SDK" >&2; exit 1; }

# Select the SoC and write the default config + regenerate the per-core headers.
# -r is deterministic (default config), so a clean clone reproduces it exactly.
"$VENV_PY" "$SDK/ameba.py" soc "$SOC" >/dev/null
"$VENV_PY" "$SDK/ameba.py" menuconfig "$SOC" -r >/dev/null

GEN="$SDK/build_$SOC/menuconfig/project_$AP_PROJECT/platform_autoconf.h"
if [ ! -f "$GEN" ]; then
  echo "ameba_gen_autoconf.sh: generated autoconf not found: $GEN" >&2
  exit 1
fi

# Stage with an include guard so the force-included override in ameba_lwip_off.h
# survives the SDK's repeated re-#includes of this header.
mkdir -p "$(dirname "$OUT")"
{
  echo "/* Auto-generated by ameba_gen_autoconf.sh from the SDK menuconfig."
  echo " * DO NOT EDIT and DO NOT COMMIT -- regenerated on every build so the"
  echo " * SDK Kconfig stays the single source of truth.  The include guard is"
  echo " * added here (the SDK header has none) so ameba_lwip_off.h's"
  echo " * #undef CONFIG_LWIP_LAYER survives the SDK's repeated re-#includes."
  echo " */"
  echo ""
  echo "#ifndef __AMEBA_PLATFORM_AUTOCONF_H__"
  echo "#define __AMEBA_PLATFORM_AUTOCONF_H__"
  echo ""
  cat "$GEN"
  echo ""
  echo "#endif /* __AMEBA_PLATFORM_AUTOCONF_H__ */"
} > "$OUT"

echo "GEN: platform_autoconf.h <- SDK menuconfig ($SOC project_$AP_PROJECT)"
