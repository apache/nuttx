/****************************************************************************
 * Name: stm32_bringup
 *
 * Description:
 *   Perform architecture-specific initialization
 *
 *   CONFIG_BOARD_LATE_INITIALIZE=y :
 *     Called from board_late_initialize().
 *
 *   CONFIG_BOARD_LATE_INITIALIZE=n && CONFIG_BOARDCTL=y :
 *     Called from the NSH library
 *
 ****************************************************************************/

int stm32_bringup(void)
{
  int ret = OK;

#if defined(CONFIG_I2C) && defined(CONFIG_SYSTEM_I2CTOOL)
  stm32_i2ctool();
#endif

#ifdef CONFIG_FS_PROCFS
  /* Mount the procfs file system */

  ret = nx_mount(NULL, STM32_PROCFS_MOUNTPOINT, "procfs", 0, NULL);
  if (ret < 0)
    {
      syslog(LOG_ERR, "ERROR: Failed to mount procfs at %s: %d\n",
             STM32_PROCFS_MOUNTPOINT, ret);
    }
#endif

#ifdef CONFIG_PWM
  /* initialize PWM and register the PWM device. */

  ret = stm32_pwm_setup();
  if (ret < 0)
    {
      syslog(LOG_ERR, "ERROR: stm32_pwm_setup() failed: %d\n", ret);
    }
#endif

#ifdef CONFIG_AUDIO_CS4344
  /* Configure CS4344 audio as /dev/pcm0 on I2S2 */

  ret = board_cs4344_initialize(1, 2);
  if (ret != OK)
    {
      syslog(LOG_ERR, "Failed to initialize CS4344 audio: %d\n", ret);
    }
#endif

#ifdef CONFIG_BOARD_MEADOW_F7_CORE_COMPUTE
  /* Initialize all devices in the F7 Core Compute */

  ret = init_corecomp();
  if (ret < 0)
    {
      syslog(LOG_ERR, "ERROR: F7 Core Compute init failed: %d\n", ret);
    }
#endif

#ifdef CONFIG_BOARD_MEADOW_PROJECTLAB
  /* Initialize all devices in the ProjectLab */

  ret = init_projectlab();
  if (ret < 0)
    {
      syslog(LOG_ERR, "ERROR: ProjectLab init failed: %d\n", ret);
    }
#endif

  return ret;
}
